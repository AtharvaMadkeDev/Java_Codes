package Java_Basics;

public class Data_types {

	public static void main(String[] args) {
		
		/**
		 * 1) Primitive Data-types
		 */
		
		byte b = 2;
		System.out.println(b+ ": This value is in bytes");
		
		short s = 45;
		System.out.println(s+ ": This value is in short");
		
		int i = 90;
		System.out.println(i+ ": This value is in int");
		
		long l = 1010;
		System.out.println(l+ ": This value is in long");
		
		float f = 10.90f;
		System.out.println(f+ ": This value is in float");
		
		double d = 10.90;
		System.out.println(d+ ": This value is in double");
		
		char c = 'C';
		System.out.println(c+ ": This value is in char");
		
		boolean boo = true;
		System.out.println(boo+ ": This value is in boolean");
		
	}

}

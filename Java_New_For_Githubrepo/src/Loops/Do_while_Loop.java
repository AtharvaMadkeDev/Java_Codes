package Loops;

public class Do_while_Loop {

	public static void main(String[] args) {
		
		/**
		 * Syntax :
		 * 
		 * initialization;
		 * do
		 * {
		 * 	//code to execute
		 * 	updation;
		 * }
		 * 	while(updation);
		 */
		
		int i=1;
		do
		{
			System.out.println(i);
			i++;
		}
		while(i<=5);
	}

}

package Basic_logical_programs;

public class swapping_two_nos {

	public static void main(String[] args) {
		
		int a=10, b=20;
		int c;
		
		System.out.println("Before:");
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println();
		c=a;
		a=b;
		b=c;
		
		System.out.println("After:");
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println();
	}

}
